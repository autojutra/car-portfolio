export type Lang = "pl" | "en";

export function getLang(value?: string): Lang {
  return value === "en" ? "en" : "pl";
}

export function buildLangHref(path: string, lang: Lang) {
  return `${path}${path.includes("?") ? "&" : "?"}lang=${lang}`;
}

